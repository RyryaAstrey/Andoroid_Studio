public class Variable {
    public static void main(String[] args) {
        int month; // 変数を宣言
        month = 9; // 宣言した変数にデータを代入
        int day = 16; // 変数を初期化（宣言と代入を同時にしている）

        System.out.println("このチャンネルは、");
        System.out.println(month + "月" + day + "日に公開しました！"); // 変数の参照をしている（処理の中に変数名を入れると参照する）
    }
}
